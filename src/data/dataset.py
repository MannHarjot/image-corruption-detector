"""Custom PyTorch Dataset for the image corruption detection task.

Loads samples from the metadata CSV produced by ``generate_dataset.py``
and returns (image_tensor, label_index, metadata_dict) tuples.
"""

from pathlib import Path
from typing import Callable, Dict, List, Optional, Tuple

import pandas as pd
import torch
from PIL import Image
from torch.utils.data import DataLoader, Dataset
from torchvision import transforms

from src.utils.logger import get_logger

logger = get_logger(__name__)

CLASS_NAMES: List[str] = [
    "clean",
    "gaussian_blur",
    "gaussian_noise",
    "salt_pepper",
    "jpeg_artifacts",
    "brightness",
    "contrast",
]
CLASS_TO_IDX: Dict[str, int] = {name: idx for idx, name in enumerate(CLASS_NAMES)}
IDX_TO_CLASS: Dict[int, str] = {idx: name for name, idx in CLASS_TO_IDX.items()}


def get_default_transforms(
    image_size: int = 224,
    split: str = "train",
    normalize_mean: Tuple[float, float, float] = (0.485, 0.456, 0.406),
    normalize_std: Tuple[float, float, float] = (0.229, 0.224, 0.225),
) -> transforms.Compose:
    """Build standard torchvision transform pipelines.

    Training transforms include random horizontal flip and color jitter for
    light augmentation. Validation/test transforms only resize and normalize.

    Args:
        image_size: Target spatial size (height == width).
        split: One of ``"train"``, ``"val"``, or ``"test"``.
        normalize_mean: Per-channel mean for normalization (ImageNet defaults).
        normalize_std: Per-channel std for normalization (ImageNet defaults).

    Returns:
        Composed torchvision transform pipeline.
    """
    base = [
        transforms.Resize((image_size, image_size)),
        transforms.ToTensor(),
        transforms.Normalize(mean=list(normalize_mean), std=list(normalize_std)),
    ]
    if split == "train":
        augment = [
            transforms.RandomHorizontalFlip(p=0.5),
            transforms.ColorJitter(brightness=0.1, contrast=0.1),
        ]
        pipeline = [transforms.Resize((image_size, image_size))] + augment + [
            transforms.ToTensor(),
            transforms.Normalize(mean=list(normalize_mean), std=list(normalize_std)),
        ]
        return transforms.Compose(pipeline)
    return transforms.Compose(base)


class CorruptionDataset(Dataset):
    """PyTorch Dataset for the image corruption detection task.

    Reads from the metadata CSV generated by ``generate_dataset.py`` and
    returns (image_tensor, label_index, metadata_dict) tuples.

    Args:
        metadata_csv: Path to ``metadata.csv``.
        split: Dataset split — ``"train"``, ``"val"``, or ``"test"``.
            Pass ``None`` to load all splits.
        transform: Optional torchvision transform applied to each PIL image.
            If ``None``, ``get_default_transforms(split=split)`` is used.

    Example:
        >>> ds = CorruptionDataset("data/metadata.csv", split="val")
        >>> img_tensor, label, meta = ds[0]
    """

    def __init__(
        self,
        metadata_csv: Path,
        split: Optional[str] = "train",
        transform: Optional[Callable] = None,
    ) -> None:
        metadata_csv = Path(metadata_csv)
        if not metadata_csv.exists():
            raise FileNotFoundError(
                f"Metadata CSV not found: {metadata_csv}. "
                "Run generate_dataset.py first."
            )

        df = pd.read_csv(metadata_csv)

        if split is not None:
            if split not in ("train", "val", "test"):
                raise ValueError(f"split must be 'train', 'val', or 'test'; got {split!r}")
            df = df[df["split"] == split].reset_index(drop=True)

        if len(df) == 0:
            raise ValueError(
                f"No samples found for split={split!r} in {metadata_csv}."
            )

        self._df = df
        self._split = split
        self._transform = transform or get_default_transforms(split=split or "test")

        logger.info(
            "CorruptionDataset loaded: split=%s, samples=%d",
            split,
            len(self._df),
        )

    def __len__(self) -> int:
        """Return the number of samples in this split."""
        return len(self._df)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, int, Dict]:
        """Fetch a single sample.

        Args:
            idx: Integer index into the dataset.

        Returns:
            Tuple of:
                - ``image`` (torch.Tensor): Normalized image tensor [C, H, W].
                - ``label`` (int): Class index.
                - ``meta`` (dict): Row metadata from the CSV.
        """
        row = self._df.iloc[idx]
        filepath = Path(row["filepath"])
        label = int(row["label"])
        meta = {
            "filepath": str(filepath),
            "corruption_type": row["corruption_type"],
            "severity": int(row["severity"]),
            "split": row["split"],
        }

        image = Image.open(filepath).convert("RGB")
        image_tensor = self._transform(image)
        return image_tensor, label, meta

    @property
    def class_names(self) -> List[str]:
        """Ordered list of class name strings."""
        return CLASS_NAMES

    @property
    def num_classes(self) -> int:
        """Number of distinct classes."""
        return len(CLASS_NAMES)

    def class_distribution(self) -> Dict[str, int]:
        """Return count of samples per class in this split.

        Returns:
            Dictionary mapping class name to sample count.
        """
        counts = self._df["corruption_type"].value_counts().to_dict()
        return {name: counts.get(name, 0) for name in CLASS_NAMES}


def create_dataloaders(
    metadata_csv: Path,
    batch_size: int = 64,
    image_size: int = 224,
    num_workers: int = 4,
    pin_memory: bool = True,
    normalize_mean: Tuple[float, float, float] = (0.485, 0.456, 0.406),
    normalize_std: Tuple[float, float, float] = (0.229, 0.224, 0.225),
) -> Dict[str, DataLoader]:
    """Factory function to create train/val/test DataLoaders.

    Args:
        metadata_csv: Path to the metadata CSV.
        batch_size: Number of samples per batch.
        image_size: Spatial size to resize images to.
        num_workers: Number of worker processes for loading.
        pin_memory: Whether to pin memory for faster GPU transfer.
        normalize_mean: Per-channel mean for normalization.
        normalize_std: Per-channel std for normalization.

    Returns:
        Dictionary with keys ``"train"``, ``"val"``, ``"test"`` mapping to
        :class:`torch.utils.data.DataLoader` instances.

    Example:
        >>> loaders = create_dataloaders("data/metadata.csv", batch_size=64)
        >>> for imgs, labels, meta in loaders["train"]:
        ...     ...
    """
    loaders: Dict[str, DataLoader] = {}

    for split in ("train", "val", "test"):
        transform = get_default_transforms(
            image_size=image_size,
            split=split,
            normalize_mean=normalize_mean,
            normalize_std=normalize_std,
        )
        dataset = CorruptionDataset(
            metadata_csv=metadata_csv,
            split=split,
            transform=transform,
        )
        shuffle = split == "train"
        loaders[split] = DataLoader(
            dataset,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            pin_memory=pin_memory,
            drop_last=shuffle,  # drop incomplete batch only during training
        )
        logger.info(
            "DataLoader[%s]: %d batches of size %d",
            split,
            len(loaders[split]),
            batch_size,
        )

    return loaders
